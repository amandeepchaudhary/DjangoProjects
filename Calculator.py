a = float(input('Enter First Number:'))
b = input('Select What you want to do with the Number +,-,*,/ :')
c = float(input('Enter Second Number:'))

if b=='+':
    print('Your Answer is:', a+c)
if b=='-':
    print('Your Answer is:', a-c)
if b=='*':
    print('Your Answer is:', a*c)
if b=='/':
    print('Your Answer is:', a/c)